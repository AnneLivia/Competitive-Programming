#include <iostream>
#include <map>

using namespace std;

int main()
{
    map<string,char>m = {
        {"=.===",'a'},
        {"===.=.=.=",'b'},
        {"===.=.===.=",'c'},
        {"===.=.=",'d'},
        {"=",'e'},
        {"=.=.===.=",'f'},
        {"===.===.=",'g'},
        {"=.=.=.=",'h'},
        {"=.=",'i'},
        {"=.===.===.===",'j'},
        {"===.=.===",'k'},
        {"=.===.=.=",'l'},
        {"===.===",'m'},
        {"===.=",'n'},
        {"===.===.===",'o'},
        {"=.===.===.=",'p'},
        {"===.===.=.===",'q'},
        {"=.===.=",'r'},
        {"=.=.=",'s'},
        {"===",'t'},
        {"=.=.===",'u'},
        {"=.=.=.===",'v'},
        {"=.===.===",'w'},
        {"===.=.=.===",'x'},
        {"===.=.===.===",'y'},
        {"===.===.=.=",'z'}
    };

    int t, qp;
    string s, aux, frase;
    cin >> t;
    cin.ignore();
    while(t--) {
        aux = frase = "";
        qp = 0;
        cin >> s;
        s+="...";
        for(int i = 0; i < (int)s.size() - 1; i++) {
            if(s[i] == '.')
                qp++;
            else
                qp = 0;

            if(qp == 6)
                frase+=" ";

            if(s[i] == '.' && s[i+1] != '.' && s[i] == '.' && s[i-1] != '.' || s[i] != '.') {
                aux+=s[i];
            } else {
                if(aux != "")
                    frase+=m.at(aux);
                aux = "";
            }
        }
        cout << frase << endl;
    }
    return 0;
}
