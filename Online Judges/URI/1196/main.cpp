#include <iostream>
#include <map>

using namespace std;

int main()
{
    map<char,char>m = {
        {'S','A'}, {'X','Z'},
        {'W','Q'}, {'E','W'},
        {'D','S'}, {'C','X'},
        {'R','E'}, {'F','D'},
        {'V','C'}, {'T','R'},
        {'H','G'}, {'B','V'},
        {'Y','T'}, {'J','H'},
        {'N','B'}, {'M','N'},
        {',','M'}, {'.',','},
        {'/','.'}, {'K','J'},
        {'L','K'}, {';','L'},
        {'\'',';'}, {'U','Y'},
        {'I','U'}, {'O','I'},
        {'P','O'}, {'[','P'},
        {']','['}, {'2','1'},
        {'3','2'}, {'4','3'},
        {'5','4'}, {'6','5'},
        {'8','7'}, {'7','6'},
        {'8','7'}, {'9','8'},
        {'0','9'}, {'-','0'},
        {'=','-'}, {'G','F'},
        {'1','`'}, {'\\',']'},
        {' ',' '}
    };
    string s, ns;
    while(getline(cin,s)) {
        ns = "";
        for(int i = 0; i < (int)s.size(); i++) {
            if(m.find(s[i]) != m.end())
                ns+=m.at(s[i]);
        }
        cout << ns << endl;
    }
    return 0;
}
